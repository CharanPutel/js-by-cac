let rollNumber = 33
console.log(typeof rollNumber);
//result: number

//Now we will convert number to string
let rollNumberString = String(rollNumber)
console.log(typeof rollNumberString);
//result: string

//Now we will convert string to number
let stringToNumber = Number(rollNumberString)
console.log(typeof stringToNumber);
//result: number

//Now we will convert number to boolean
let numberToBoolean = Boolean(rollNumber)
console.log(numberToBoolean); // true
console.log(typeof numberToBoolean);
//result: boolean

//Now we will convert boolean to number
let booleanToNumber = Number(numberToBoolean)
console.log(typeof booleanToNumber);
//result: number    

